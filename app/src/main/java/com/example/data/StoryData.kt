enum class Genre(val key: String, val npLabel: String, val enLabel: String) {
    GHOST("Ghost", "भूत", "Ghost"),
    FOLKLORE("Folklore", "लोककथा", "Folklore"),
    MYSTERY("Mystery", "रहस्य", "Mystery"),
    UNDERRATED("Underrated", "अल्पचर्चित", "Underrated"),
    COMMUNITY("Community", "सामुदायिक", "Community");

    companion object {
        fun fromKey(key: String): Genre {
            return entries.find { it.key.equals(key, ignoreCase = true) } ?: GHOST
        }
    }
}

